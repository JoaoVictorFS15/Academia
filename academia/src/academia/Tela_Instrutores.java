    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        instrutores ins = new instrutores();
        try {
            ins.setId((int)jTable1.getValueAt
        (jTable1.getSelectedRow(), 0));
            ins.setNome(txtNome.getText().trim());
            ins.setTelefone(Integer.parseInt( txtTelefone.getText().trim()));
            ins.setEndereco(txtEndereco.getText().trim());
            ins.setBairro(txtBairro.getText().trim());
               ins.atualizar(ins);
            JOptionPane.showMessageDialog(null, "Instrutor atualizado com sucesso!");
            txtNome.setText("");
            txtTelefone.setText("");
            txtEndereco.setText("");
            txtBairro.setText("");
            LerJTable();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao atualizar: " + ex.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

