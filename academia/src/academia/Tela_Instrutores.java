    private void PreencherInstrutores() throws SQLException {
        instrutores prem = new instrutores();
        ArrayList listaPremiacoes = new ArrayList();
        listaPremiacoes = prem.ler();
        
        for (int i = 0; i < listaPremiacoes.size(); i++) 
        {
            prem = (instrutores)listaPremiacoes.get(i);
            jComboBox1.addItem(prem.toString());
        }
    }

