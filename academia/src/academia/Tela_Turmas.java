    public void LerJTable () throws SQLException
    {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setNumRows(0);
        jTable1.setRowSorter(new TableRowSorter(modelo));
        //Preciso cadastrar o objeto que representa a linha da tabela e depois 
        //percorrer a lista com os clientes e pegar os dados e adicionar Ã  linha 
        //da tabela.
        Object linha[] = new Object[5];
        
        turmas tur = new turmas();
        ArrayList Turmas = new ArrayList();
        Turmas = tur.ler();
        
        for (int i = 0; i < Turmas.size(); i++) 
        {
            tur = (turmas)Turmas.get(i);
            linha[0] = tur.getId();
            linha[1] = tur.getDuracao();
            linha[2] = tur.getHora_Inicio();
            linha[3] = tur.getModalidade().getNome();
            linha[4] = tur.getDiasemana().getNome();
            
            
            
            //Adicionando a linha no JTable
            modelo.addRow(linha);
        }
    }

