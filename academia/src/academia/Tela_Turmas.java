    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      turmas tur = new turmas();
    
       
       
       modalidades pMod = new modalidades();
        
       pMod = (modalidades) ccModalidade.getSelectedItem();
   
       
     
      diasemana pDia = new diasemana();
      pDia = (diasemana) ccDia.getSelectedItem();
       
       
  
       
       
  
        try {
            tur.cadastrar(Integer.parseInt(txtDuracao.getText().trim()),Time.valueOf( txtHora.getText()),pDia, pMod);
            JOptionPane.showMessageDialog(null, "Turma cadastrado com sucesso!");
            
            txtDuracao.setText("");
            txtHora.setText("");
           
            
            LerJTable();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao Cadastrar: " + ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
          turmas tur = new turmas();
        try {
            tur.setId((int)jTable1.getValueAt(jTable1.getSelectedRow(), 0));
            tur.setDiasemana((diasemana)ccDia.getSelectedItem());
            tur.setModalidade((modalidades)ccModalidade.getSelectedItem());
            tur.setDuracao((Integer.parseInt(txtDuracao.getText().trim())));
            tur.setHora_Inicio(Time.valueOf( txtHora.getText().trim()));
            tur.atualizar();
            
            JOptionPane.showMessageDialog(null, "Turma atualizado com sucesso!");
            
            txtDuracao.setText("");
            txtHora.setText("");
            
            
            LerJTable();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao atualizar: " + ex.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

