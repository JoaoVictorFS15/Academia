    private void PreencherDias() throws SQLException 
    {
        diasemana dia = new diasemana();
        ArrayList DiasLista = new ArrayList();
        DiasLista = dia.ler();
        
        for (int i = 0; i < DiasLista.size(); i++) 
        {
            dia = (diasemana)DiasLista.get(i);
            ccDia.addItem(dia);
        }
    }
     private void PreencherModalidade() throws SQLException 
    {
        modalidades mod = new modalidades();
        ArrayList ModLista = new ArrayList();
        ModLista = mod.ler();
        
        for (int i = 0; i < ModLista.size(); i++) 
        {
            mod = (modalidades)ModLista.get(i);
            ccModalidade.addItem(mod);
        }
    }

